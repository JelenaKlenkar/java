package primitive;

public class Task10 {

	public static void main(String[] args) {
		// Calculate molecular mass of iron(III)oxide

		double Fe = 55.85;
		double O = 16.00;

		double Mr = 2 * Fe + 3 * O;

		System.out.println("2" + "*" + Fe + "+" + "3" + "*" + O + "=" + Mr);
		System.out.println("Molecular mass of iron(III)oxide is" + " " + Mr + ".");
	}

}
