package src.primitive;

public class Task11 {

	public static void main(String[] args) {
		// calculate volume of sodium hydroxide if mass is 250g, and density 1,328g/cm3

		int m = 250;
		double d = 1.328;

		double V = m / d;

		System.out.println(m + "/" + d + "=" + V);
		System.out.println("Volume of sodium hydroxide is" + " " + V +" " +"cm3"+".");

	}

}
